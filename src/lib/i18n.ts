export function useI18n() {
  const [locale, setLocale] = useState(getLocaleFromSettings);

  useEffect(() => {
    const handler = () => setLocale(getLocaleFromSettings());
    window.addEventListener('storage', handler);
    const interval = setInterval(() => {
      const current = getLocaleFromSettings();
      setLocale(prev => {
        if (prev.language !== current.language || prev.currency !== current.currency) return current;
        return prev;
      });
    }, 500);
    return () => { window.removeEventListener('storage', handler); clearInterval(interval); };
  }, []);

  const t = useCallback((key: string): string => {
    return translations[locale.language]?.[key] || translations['pt-BR'][key] || key;
  }, [locale.language]);

  const fmt = useCallback((v: number): string => {
    return v.toLocaleString(locale.language, { style: 'currency', currency: locale.currency });
  }, [locale.language, locale.currency]);

  return { t, fmt, language: locale.language, currency: locale.currency };
}
