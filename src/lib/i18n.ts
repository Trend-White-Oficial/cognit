export type Language = 'pt-BR' | 'en-US' | 'es';
export type Currency = 'BRL' | 'USD';

const SETTINGS_KEY = 'cognit_user_settings_v1';

function getLocaleFromSettings(): { language: Language; currency: Currency } {
  try {
    const raw = localStorage.getItem(SETTINGS_KEY);
    if (!raw) return { language: 'pt-BR', currency: 'BRL' };
    const parsed = JSON.parse(raw);
    return {
      language: parsed?.locale?.language || 'pt-BR',
      currency: parsed?.locale?.currency || 'BRL',
    };
  } catch {
    return { language: 'pt-BR', currency: 'BRL' };
  }
}

